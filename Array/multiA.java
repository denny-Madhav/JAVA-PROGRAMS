public class multiA {
    
    //multidimentional array

public static void main(String arg[])
{
int arr[][]={
{10,20,30},
{40,50,60},
{70,86,80}
};
for(int i=0;i<3;i++)
{
for(int j=0;j<3;j++)
{
System.out.print(arr[i][j]+" ");
}
System.out.println();
}
}
}
    
